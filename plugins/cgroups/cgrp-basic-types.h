typedef   signed char  s8_t;
typedef unsigned short u16_t;
typedef   signed short s16_t;
typedef u_int32_t      u32_t;
typedef   int32_t      s32_t;
typedef u_int64_t      u64_t;
typedef   int64_t      s64_t;

